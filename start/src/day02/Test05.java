package day02;

public class Test05 {
	public static void main(String[] args) {
		// 3.14를 float 타입 변수 pi에 기억해보자.
		
		float pi ;	// 변수 선언
		
		pi = 3.14f;
		
		System.out.println("pi : " + pi);
		
	}
	
	/*
		참고 ]
			개발자가 입력하는 정수형 데이터는 
			특별한 조작없이 모든 데이터형이 기억될 수 있다.
			
			예 ]
				int a = 100;
				byte b = 100;
				short c = 100;
				long d = 100;
	 */
}
